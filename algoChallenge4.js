/**
 * Write a function that defines a variable called 
 * isWorking that is assigned a boolean. Print different 
 * messages based on the value of isWorking.
 */

//--------------------My Solution-------------------------
function isWorking(value){
    if(value == "Yes"){
        let output = true;
        console.log(output);
    }else{
        output = false;
        console.log(output)
    }
}
isWorking('Yes'); //Returns: true
isWorking('Cheese');//Returns: false

