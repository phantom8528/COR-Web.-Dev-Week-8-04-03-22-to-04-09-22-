/**
 * Write a function that defines a variable called 
 * firstName, then prints it to the terminal screen 
 * 5 times.
 */

//---------------------------My Solution-----------------------------
function timesFive(firstName){
    let i=0;
    while(i<5){
        console.log(`My Name is ${firstName}`);
        i++;
    }
}
timesFive(`Gabriel`);
